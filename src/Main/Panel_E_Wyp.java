    public void Item(){
        JD_z.getDateEditor().addPropertyChangeListener(new PropertyChangeListener() {
            //@Override
            public void propertyChange(PropertyChangeEvent e) {
                if ("date".equals(e.getPropertyName())) {
                    //System.out.println((Date) e.getNewValue());
                    if(JD_z.getDate() != null && Date_W!= null){
                        if(Date_W.compareTo(JD_z.getDate())<0){
                            int dni = calculateDifference(Date_W,JD_z.getDate());
                            Search_ID_cen(dni, ID_M);
                            error_date.setVisible(false);
                            er = 0;
                        }else{
                            error_date.setVisible(true);
                            er = 1;
                        }                        
                    }
                }
            }
        });
    }
    
    public void Search_ID_cen(int dni, String ID){
        try {
            Mysql sql = new Mysql();
            String cena;
            cena = sql.Mag_Cena(ID);
            int cen = Integer.parseInt(cena);
            cen *= dni;
            L_koszt.setText("Koszt wypozyczalnia: "+cen+" złotych");
        } catch (SQLException ex) {
            Logger.getLogger(Panel_E_Wyp.class.getName()).log(Level.SEVERE, null, ex);
            Plik p = new Plik();
            try {
                p.zapisPliku(ex.toString());
            } catch (IOException ex1) {
                Logger.getLogger(Panel_E_Klient.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
    }
    
