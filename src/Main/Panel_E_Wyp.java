    public void Baza(){
        try {
            Mysql sql = new Mysql();
            String baza[] = new String[5];
            baza = sql.Wyp_Search(ID);
            ID_M = baza[2];
            
            String baza_k = new String();
            String baza_m = new String();
            baza_k = sql.Client_Search_Name(baza[1]);
            baza_m = sql.Mag_Search_Name(baza[2]);
            String dw = baza[3];
            String d_z = baza[4];
            SimpleDateFormat ft = new SimpleDateFormat("yyyy-MM-dd");
            Date_W = ft.parse(dw);
            Date dz = ft.parse(d_z);
            L_Klient.setText(baza_k);
            L_Mag.setText(baza_m);
            L_Dw.setText(dw);
            JD_z.setDate(dz);
            
        } catch (SQLException ex) {
            Logger.getLogger(Panel_E_Wyp.class.getName()).log(Level.SEVERE, null, ex);
            Plik p = new Plik();
            try {
                p.zapisPliku(ex.toString());
            } catch (IOException ex1) {
                Logger.getLogger(Panel_E_Klient.class.getName()).log(Level.SEVERE, null, ex1);
            }
        } catch (ParseException ex) {
            Logger.getLogger(Panel_E_Wyp.class.getName()).log(Level.SEVERE, null, ex);
            Plik p = new Plik();
            try {
                p.zapisPliku(ex.toString());
            } catch (IOException ex1) {
                Logger.getLogger(Panel_E_Klient.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
    }
    
   

