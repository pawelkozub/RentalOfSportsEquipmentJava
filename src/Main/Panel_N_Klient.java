    private void B_save_new_KActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_save_new_KActionPerformed
        int n=6;
        int spr[] = new int[n];
        
        if(!TF_imie.getText().equals("")){
            spr[0] = 1;
            L_S_imie.setVisible(false);
        }else{
            spr[0] = 0;
            L_S_imie.setVisible(true);
        }
        
        if(!TF_nazwisko.getText().equals("")){
            spr[1] = 1;
            L_S_nazwisko.setVisible(false);
        }else{
            spr[1] = 0;
            L_S_nazwisko.setVisible(true);
        }
        
        if(!TF_ulica.getText().equals("")){
            spr[2] = 1;
            L_S_ulica.setVisible(false);
        }else{
            spr[2] = 0;
            L_S_ulica.setVisible(true);
        }
        
        if(!TF_nr.getText().equals("")){
            spr[3] = 1;
            L_S_nr.setVisible(false);
        }else{
            spr[3] = 0;
            L_S_nr.setVisible(true);
        }
        
        if(!TF_kod.getText().equals("")){
            spr[4] = 1;
            L_S_kod.setVisible(false);
        }else{
            spr[4] = 0;
            L_S_kod.setVisible(true);
        }
        
        if(!TF_miasto.getText().equals("")){
            spr[5] = 1;
            L_S_miasto.setVisible(false);
        }else{
            spr[5] = 0;
            L_S_miasto.setVisible(true);
        }
        
        int SUM =0;
        for(int i=0;i<n;i++){
            SUM += spr[i];
        }
        
        if(SUM == n){
                Save_Baza(TF_imie.getText(), TF_nazwisko.getText(), TF_ulica.getText(), TF_nr.getText(), TF_kod.getText(), TF_miasto.getText());
                Frame_Save_Baza k = new Frame_Save_Baza();
                k.main(args);
                this.setVisible(false);
                Clear();
        }
    }//GEN-LAST:event_B_save_new_KActionPerformed

    public void Save_Baza(String imie, String nazw, String ul, String nr, String kod, String city){
        try {
            Mysql sql = new Mysql();
            sql.Client_Add(imie, nazw, ul, nr, kod, city);
        } catch (SQLException ex) {
            Logger.getLogger(Panel_N_Klient.class.getName()).log(Level.SEVERE, null, ex);
            Plik p = new Plik();
            try {
                p.zapisPliku(ex.toString());
            } catch (IOException ex1) {
                Logger.getLogger(Panel_E_Klient.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
    }
    
